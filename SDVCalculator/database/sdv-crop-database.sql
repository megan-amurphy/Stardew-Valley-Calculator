DROP TABLE IF EXISTS crops, seeds, purchase_location, crop_seed, seed_location CASCADE;
DROP SEQUENCE IF EXISTS crop_serial, seed_serial, purchase_location_serial, seed_location_id;

CREATE SEQUENCE crop_serial;
CREATE SEQUENCE seed_serial;
CREATE SEQUENCE purchase_location_serial;

--creating the crop table

CREATE TABLE crops (
	crop_id int NOT NULL DEFAULT nextval('crop_serial'),
	crop_name varchar(50) NOT NULL UNIQUE,
	crop_season varchar(50) NOT NULL,
	normal_price int NOT NULL,
	silver_price int NOT NULL,
	gold_price int NOT NULL,
	iridium_price int NOT NULL,
	CONSTRAINT pk_crop PRIMARY KEY(crop_id)
);

--creating the seed table
CREATE TABLE IF NOT EXISTS seeds (
	seed_id int NOT NULL DEFAULT nextval('seed_serial'),
	seed_name varchar(50) NOT NULL,
	CONSTRAINT pk_seed PRIMARY KEY(seed_id)
);
CREATE TABLE IF NOT EXISTS purchase_location (
	purchase_location_id int NOT NULL DEFAULT nextval('purchase_location_serial'),
	purchase_location_name varchar(150) NOT NULL UNIQUE,
	CONSTRAINT pk_purchase_location PRIMARY KEY(purchase_location_id)	
);

CREATE TABLE IF NOT EXISTS crop_seed (
	crop_id int NOT NULL,
	seed_id int NOT NULL, 
	CONSTRAINT pk_crop_seed PRIMARY KEY(crop_id, seed_id),
	CONSTRAINT fk_crop_seed_crop FOREIGN KEY(crop_id) REFERENCES crops(crop_id),
	CONSTRAINT fk_crop_seed_seed FOREIGN KEY(seed_id) REFERENCES seeds(seed_id)
);

CREATE TABLE IF NOT EXISTS seed_location (
	seed_location_id serial PRIMARY KEY, 
	location_id int NOT NULL,
	seed_id int NOT NULL,
	seed_price_lower int NOT NULL,
	seed_price_upper int NOT NULL,
	item_non_gold varchar(100),
	is_out_of_season boolean NOT NULL,
	CONSTRAINT fk_seed_location_location FOREIGN KEY(location_id) REFERENCES purchase_location(purchase_location_id),
	CONSTRAINT fk_seed_location_seed FOREIGN KEY(seed_id) REFERENCES seeds(seed_id)
);

